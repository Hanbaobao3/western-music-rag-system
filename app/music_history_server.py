def identify_period(question):
    """识别问题对应的音乐时期"""
    question_lower = question.lower()

    # 古代与中世纪
    if any(keyword in question_lower for keyword in ['古希腊', '古罗马', '毕达哥拉斯', '柏拉图', '亚里士多德', '亚里士多', '格里高利', '圣咏', '弥撒曲', '游吟诗人', '奥尔加农', '经文歌', '等节奏', '新艺术', '圣母院', '中世纪']):
        return 'ancient_medieval'

    # 文艺复兴
    elif any(keyword in question_lower for keyword in ['文艺复兴', '迪法伊', '勃艮第', '奥克冈', '帕勒斯特里那', '宗教改革', '马丁·路德', '牧歌', '尚松', '维瓦尔第']):
        return 'renaissance'

    # 巴洛克
    elif any(keyword in question_lower for keyword in ['巴洛克', '佩里', '蒙泰韦尔迪', '吕利', '拉莫', '珀塞尔', '科雷利', '通奏低音', '歌剧的诞生', '协奏曲', '奏鸣曲']):
        return 'baroque'

    # 古典主义
    elif any(keyword in question_lower for keyword in ['古典主义', '海顿', '莫扎特', '贝多芬', '华美风格', '敏感风格', '曼海姆', '斯塔米茨', '回旋曲', '变奏曲']):
        return 'classical'

    # 浪漫主义
