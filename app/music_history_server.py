class RAGHandler(BaseHTTPRequestHandler):
    def _set_headers(self, status_code=200):
        self.send_response(status_code)
        self.send_header('Content-type', 'application/json; charset=utf-8')
        self.send_header('Access-Control-Allow-Origin', '*')
        self.send_header('Access-Control-Allow-Methods', 'GET, POST, OPTIONS')
        self.send_header('Access-Control-Allow-Headers', 'Content-Type')
        self.end_headers()

    def do_OPTIONS(self):
        self._set_headers()
        self.wfile.write(json.dumps({'status': 'ok'}).encode())

    def do_GET(self):
        if self.path in ['/', '/api/health']:
            self._set_headers()
            response = {
                'status': 'ok',
                'message': '西方音乐史RAG系统运行正常',
                'server': 'Python http.server',
                'version': '4.0.0',
                'knowledge_base': '完整西方音乐史知识库',
                'periods': ['古代与中世纪', '文艺复兴', '巴洛克', '古典主义', '浪漫主义', '20世纪', '核心名词解释', '考研记忆口诀']
            }
            self.wfile.write(json.dumps(response, ensure_ascii=False).encode('utf-8'))
        else:
            self._set_headers(404)
            self.wfile.write(json.dumps({'error': '路径不存在'}).encode())

    def do_POST(self):
        if self.path == '/api/query':
            try:
                content_length = int(self.headers.get('Content-Length', 0))
                if content_length > 0:
                    post_data = self.rfile.read(content_length).decode('utf-8')
                    request_data = json.loads(post_data)

                    query = request_data.get('query', '').strip()

                    if query:
                        # 识别时期并生成答案
                        period_key = identify_period(query)
                        answer = generate_answer(query, period_key)

                        self._set_headers(200)
                        response = {
                            'success': True,
                            'answer': answer,
                            'period': period_key,
                            'question_type': self._classify_question_type(query),
                            'retrieved_docs': get_mock_retrieved_docs(period_key),
                            'ai_generated': False,
                            'model': '西方音乐史知识库',
                            'timestamp': time.strftime('%Y-%m-%d %H:%M:%S')
                        }
                        self.wfile.write(json.dumps(response, ensure_ascii=False).encode('utf-8'))
                    else:
                        self._set_headers(400)
                        self.wfile.write(json.dumps({'success': False, 'error': '查询不能为空'}).encode())
                else:
                    self._set_headers(400)
                    self.wfile.write(json.dumps({'success': False, 'error': '请求体为空'}).encode())
            except Exception as e:
                self._set_headers(500)
                self.wfile.write(json.dumps({'success': False, 'error': str(e)}).encode())
        else:
            self._set_headers(404)
            self.wfile.write(json.dumps({'error': '路径不存在'}).encode())

    def _classify_question_type(self, question):
        """根据问题内容判断题型"""
        question_lower = question.lower()

        if any(keyword in question_lower for keyword in ['什么是', '定义', '名词解释', '解释', '概念']):
            return 'definition'
        elif any(keyword in question_lower for keyword in ['特点', '特征', '风格', '主要', '简述', '简答']):
            return 'short_answer'
        elif any(keyword in question_lower for keyword in ['论述', '分析', '发展', '演变', '过程', '历史', '影响', '意义', '贡献']):
            return 'essay'
        elif any(keyword in question_lower for keyword in ['对比', '比较', '区别', '差异', '相同', '不同']):
            return 'comparison'
        elif any(keyword in question_lower for keyword in ['记忆', '口诀', '考研', '总结', '要点']):
            return 'memory'
        else:
            return 'general'

